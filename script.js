let currentLang = "en";

const toggle = document.getElementById("langToggle");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const revealNodes = document.querySelectorAll(".reveal");
const navLinks = document.querySelectorAll(".nav a");
const sections = document.querySelectorAll("section[id]");

function applyLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;

  translatableNodes.forEach(node => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[lang] && dictionary[lang][key]) {
      node.textContent = dictionary[lang][key];
    }
  });

  toggle.textContent = lang === "en" ? "IT" : "EN";
}

toggle.addEventListener("click", () => {
  applyLanguage(currentLang === "en" ? "it" : "en");
});

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add("show");
    }
  });
}, { threshold: 0.16 });

revealNodes.forEach(node => revealObserver.observe(node));

const activeObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      navLinks.forEach(link => {
        link.classList.toggle("active", link.getAttribute("href") === `#${entry.target.id}`);
      });
    }
  });
}, { threshold: 0.45 });

sections.forEach(section => activeObserver.observe(section));

window.addEventListener("scroll", () => {
  const y = window.scrollY * 0.08;
  const hero = document.querySelector(".hero-inner");
  const orb1 = document.querySelector(".orb-1");
  const orb2 = document.querySelector(".orb-2");

  if (hero) hero.style.transform = `translateY(${y * 0.2}px)`;
  if (orb1) orb1.style.transform = `translateY(${y * -0.35}px)`;
  if (orb2) orb2.style.transform = `translateY(${y * 0.28}px)`;
}, { passive: true });

applyLanguage("en");
